#!/usr/bin/env node
// ============================================================
// Builds dist/washes.standalone.js — the classic <script>-tag
// build — from src/washes.js by removing the trailing ESM
// export block (a classic script cannot contain top-level
// `export`; see docs/FIXES.md §1 for the v0.98 precedent).
// Verifies the result parses as a classic script and attaches
// globalThis.Washes / globalThis.Watercolor when run.
// ============================================================
'use strict';
const fs = require('fs');
const path = require('path');
const vm = require('vm');

const root = path.join(__dirname, '..');
const src = fs.readFileSync(path.join(root, 'src', 'washes.js'), 'utf8');

const EXPORT_BLOCK = [
  'const Washes = globalThis.Washes;',
  'const Watercolor = globalThis.Watercolor;',
  'export { Washes, Watercolor };',
  'export default Washes;',
].join('\n');

if (!src.trimEnd().endsWith(EXPORT_BLOCK)) {
  console.error('src/washes.js does not end with the expected ESM export block — refusing to build.');
  process.exit(1);
}

const body = src.trimEnd().slice(0, -EXPORT_BLOCK.length).trimEnd();
const banner = `// ============================================================
// washes.standalone.js — classic <script>-tag build of washes.
// Generated by scripts/build-standalone.cjs from src/washes.js
// (trailing ESM export block removed). Attaches window.Washes
// and window.Watercolor:
//
//   <script src="engine/dist/washes.standalone.js"></script>
//   <script> const wc = Washes.create(host); </script>
//
// For bundlers / ESM, use the package entry (src/index.js).
// ============================================================
`;
const out = banner + body + '\n';

// Verify: must parse as a *classic* script (vm.Script rejects `export`) …
new vm.Script(out, { filename: 'washes.standalone.js' });

// … and attach the factories when executed.
const sandbox = {
  console,
  setTimeout, clearTimeout, setInterval, clearInterval,
  performance,
  requestAnimationFrame: (cb) => setTimeout(() => cb(Date.now()), 16),
  cancelAnimationFrame: clearTimeout,
};
vm.createContext(sandbox);
vm.runInContext(out, sandbox, { filename: 'washes.standalone.js' });
if (typeof sandbox.Washes?.create !== 'function' || typeof sandbox.Watercolor?.create !== 'function') {
  console.error('Standalone build did not attach globalThis.Washes/.Watercolor with a create().');
  process.exit(1);
}

fs.mkdirSync(path.join(root, 'dist'), { recursive: true });
fs.writeFileSync(path.join(root, 'dist', 'washes.standalone.js'), out);
console.log(`dist/washes.standalone.js written (${(out.length / 1024).toFixed(0)} KB) — classic-script parse + global attach verified.`);

// ---- timeline sidecar standalone (v1.22) ----
// Its exports are declaration-prefixed (`export const` / `export class`),
// so the classic build just strips the keyword; the module's own UMD-ish
// tail attaches window.WashesTimeline.
const tlSrc = fs.readFileSync(path.join(root, 'src', 'washes-timeline.js'), 'utf8');
const tlOut =
  `// ============================================================
// washes-timeline.standalone.js — classic <script>-tag build of the
// timeline sidecar. Generated by scripts/build-standalone.cjs from
// src/washes-timeline.js (export keywords stripped). Attaches
// window.WashesTimeline / window.washesEasings.
// ============================================================
` + tlSrc.replace(/^export /gm, '');
new vm.Script(tlOut, { filename: 'washes-timeline.standalone.js' });
const tlSandbox = { console, window: {} };
vm.createContext(tlSandbox);
vm.runInContext(tlOut, tlSandbox, { filename: 'washes-timeline.standalone.js' });
if (typeof tlSandbox.window.WashesTimeline !== 'function') {
  console.error('Timeline standalone build did not attach window.WashesTimeline.');
  process.exit(1);
}
fs.writeFileSync(path.join(root, 'dist', 'washes-timeline.standalone.js'), tlOut);
console.log(`dist/washes-timeline.standalone.js written (${(tlOut.length / 1024).toFixed(1)} KB) — classic-script parse + window attach verified.`);
